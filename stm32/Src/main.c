void playTone(int tone, int duration) {

		for (long i = 0; i < duration * 1000L; i += tone * 2) {
			HAL_GPIO_WritePin(GPIOD, GPIO_PIN_14, GPIO_PIN_SET);//digitalWrite(speakerPin, HIGH);
			DWT_Delay(tone);//delayMicroseconds(tone);
			HAL_GPIO_WritePin(GPIOD, GPIO_PIN_14, GPIO_PIN_RESET);//digitalWrite(speakerPin, LOW);
			DWT_Delay(tone);// delayMicroseconds(tone);
		}

}
void playNote(char note, int duration) {
  char names[] = { 'c', 'd', 'e', 'f', 'g', 'a', 'b', 'C', 'D', 'E', 'F', 'G', 'A' };
  int tones[] = { 1915, 1700, 1519, 1432, 1275, 1136, 1014, 956, 850, 759, 716, 637, 568 };

  // play the tone corresponding to the note name
  for (int i = 0; i < 13; i++) {
    if (names[i] == note) {
      playTone(tones[i], duration);
    }
  }
}

void playSong(void const * argument){
	while(1){
		if (statePlay == '8'){
		for (int i = 0; i < len; i++) {
			if (notes[i] == ' ') {
			  osDelay(beats[i] * tempo); // rest
			} else {
			  playNote(notes[i], beats[i] * tempo);
			}

			// pause between notes
			osDelay(tempo / 2);
		  }
		}
	}
}

